package chapter1;

// 주석 : 컴파일러가 인식하지 않는 문장
// 한 줄 주석 : // 로 시작하는 문장
// 여러 줄 주석 : /* 로 시작해서 */ 로 끝나는 문장
// 문서화 주석 : /** 로 시작해서 */ 로 끝나는 문장 (API 문서 생성에 사용됨)

// 클래스 선언부 : 자바 프로그램은 클래스 단위로 작성됨
// 클래스 이름은 대문자로 시작하는 것이 관례
public class Hello { 

    // main 메서드 선언부 : 자바 프로그램의 시작점
    public static void main(String[] args) {
        // 콘솔에 출력하는 실행문
        System.out.println("Hello, World!");
    } //end main

}   //end class
